do $$
declare
  staged_count integer;
  target_count integer;
  invalid_count integer;
begin
  select count(*) into staged_count from medication_descriptions_20260718;
  if staged_count <> 90 then
    raise exception 'Medication description staging count must be 90, got %', staged_count;
  end if;

  select count(*) into invalid_count
  from medication_descriptions_20260718
  where description_th ilike '%รอตรวจสอบ%'
     or description_th ilike '%รอตรวจทาน%'
     or char_length(description_th) < 20;

  if invalid_count <> 0 then
    raise exception 'Staged descriptions contain unresolved or too-short text: %', invalid_count;
  end if;

  select count(*) into target_count
  from public.medications as medication
  join medication_descriptions_20260718 as staged using (code)
  where medication.status = 'in_review'
    and not medication.active
    and medication.description_th ilike '%รอตรวจทาน%'
    and coalesce(cardinality(medication.source_references), 0) > 0;

  if target_count <> 90 then
    raise exception 'Expected 90 in-review placeholder targets with sources, got %', target_count;
  end if;
end;
$$;

update public.medications as medication
set
  description_th = staged.description_th,
  review_notes = concat_ws(
    E'\n',
    nullif(medication.review_notes, ''),
    'Description batch medication_descriptions_20260718_v1: สรุปข้อบ่งใช้จากเอกสาร PIL/SmPC ใน source_references; ต้องให้ผู้เชี่ยวชาญตรวจและอนุมัติก่อนเผยแพร่'
  )
from medication_descriptions_20260718 as staged
where medication.code = staged.code
  and medication.status = 'in_review'
  and not medication.active
  and medication.description_th ilike '%รอตรวจทาน%';

do $$
declare
  updated_count integer;
  remaining_placeholders integer;
  unsafe_state_count integer;
begin
  select count(*) into updated_count
  from public.medications
  where review_notes like '%medication_descriptions_20260718_v1%';

  if updated_count <> 90 then
    raise exception 'Expected 90 updated medication descriptions, got %', updated_count;
  end if;

  select count(*) into remaining_placeholders
  from public.medications
  where description_th ilike '%รอตรวจทาน%'
     or description_th ilike '%รอตรวจสอบ%';

  if remaining_placeholders <> 0 then
    raise exception 'Medication description placeholders remain: %', remaining_placeholders;
  end if;

  select count(*) into unsafe_state_count
  from public.medications
  where review_notes like '%medication_descriptions_20260718_v1%'
    and (status <> 'in_review' or active);

  if unsafe_state_count <> 0 then
    raise exception 'Description batch changed review/publication state for % records', unsafe_state_count;
  end if;
end;
$$;

